# # operator aritmatika (*,/,-,+)

a = 10
b = 20

# operator penjumlahan (+)
penjumlahan = a + b
print("a + b = ",penjumlahan)

# operator pengurangan (-)
penjumlahan = a - b
print("a - b = ",penjumlahan)

# operasi perkalian (*)
penjumlahan = a * b
print("a * b = ",penjumlahan)

# operasi pembagian (/)
penjumlahan = a / b
print("a / b = ",penjumlahan)


